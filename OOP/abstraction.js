class Car {
    constructor(brand, model) {
        this.brand = brand; // –ú–∞—Ä–∫–∞ –∞–≤—Ç–æ–º–æ–±–∏–ª—è
        this.model = model; // –ú–æ–¥–µ–ª—å –∞–≤—Ç–æ–º–æ–±–∏–ª—è
        this.speed = 0; // –¢–µ–∫—É—â–∞—è —Å–∫–æ—Ä–æ—Å—Ç—å
    }

    // –ú–µ—Ç–æ–¥ –¥–ª—è —É—Å–∫–æ—Ä–µ–Ω–∏—è –∞–≤—Ç–æ–º–æ–±–∏–ª—è
    accelerate(amount) {
        this.speed += amount;
        console.log(`${this.brand} ${this.model} is accelerating. Current speed: ${this.speed} km/h`);
    }

    // –ú–µ—Ç–æ–¥ –¥–ª—è —Ç–æ—Ä–º–æ–∂–µ–Ω–∏—è –∞–≤—Ç–æ–º–æ–±–∏–ª—è
    brake(amount) {
        this.speed = Math.max(0, this.speed - amount);
        console.log(`${this.brand} ${this.model} is braking. Current speed: ${this.speed} km/h`);
    }

    // –ú–µ—Ç–æ–¥ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–π —Å–∫–æ—Ä–æ—Å—Ç–∏
    getCurrentSpeed() {
        return this.speed;
    }
}

// –°–æ–∑–¥–∞–Ω–∏–µ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞ Car
const myCar = new Car('Toyota', 'Corolla');

// –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –º–µ—Ç–æ–¥–æ–≤ –¥–ª—è –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏—è —Å –∞–≤—Ç–æ–º–æ–±–∏–ª–µ–º
myCar.accelerate(50); // Toyota Corolla is accelerating. Current speed: 50 km/h
myCar.brake(20);      // Toyota Corolla is braking. Current speed: 30 km/h
console.log(`Current Speed: ${myCar.getCurrentSpeed()} km/h`); // Current Speed: 30 km/h

